# üéµ Extraction MFCC
def extract_mfcc(file_path, max_len=SAMPLES_PER_TRACK):
    try:
        audio, sr = librosa.load(file_path, sr=SAMPLE_RATE)
        if len(audio) < max_len:
            audio = np.pad(audio, (0, max_len - len(audio)))
        else:
            audio = audio[:max_len]
        mfcc = librosa.feature.mfcc(y=audio, sr=sr, n_mfcc=40)
        return mfcc.T
    except Exception as e:
        print(f"‚ùå Erreur MFCC : {file_path} : {e}")
        return None

# üìÇ Chargement des donn√©es
def load_mfcc_data():
    X, y = [], []
    labels_map = {"belle_voix": 1, "non_belle_voix": 0}
    for folder_name, label in labels_map.items():
        folder_path = os.path.join(DATA_PATH, folder_name)
        if not os.path.exists(folder_path):
            print(f"‚ö†Ô∏è Dossier introuvable : {folder_path}")
            continue
        for file_name in os.listdir(folder_path):
            if file_name.endswith(".wav"):
                path = os.path.join(folder_path, file_name)
                mfcc_data = extract_mfcc(path)
                if mfcc_data is not None:
                    X.append(mfcc_data)
                    y.append(label)
    return np.array(X, dtype=object), np.array(y)

# ‚úÖ Chargement et padding
