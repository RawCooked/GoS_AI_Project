def predict_voice(file_path):
    mfcc = extract_mfcc(file_path)
    if mfcc is None:
        print("‚ùå MFCC non valide")
        return
    mfcc_padded = pad_sequences([mfcc], maxlen=max_length, dtype='float32', padding='post')
    mfcc_padded = mfcc_padded[..., np.newaxis]
